package day08_relational_operators;

public class ShortHandOperators {
    public static void main(String[] args) {
        int b = 4;
        // 10 times I write b++;

        b=14;
        b=b+10;

        int count = 6;
        count+=5; // count = count +5

        System.out.println(count);

        count-=10;
        System.out.println(count);
    }
}
