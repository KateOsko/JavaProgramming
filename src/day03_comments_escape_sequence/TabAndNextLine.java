package day03_comments_escape_sequence;

public class TabAndNextLine {
    public static void main(String[] args) {
        System.out.println("My name starts with 'E'");
        System.out.println("\t This was tabbed once");
        System.out.println("\t\t This was tabbed twice");
        System.out.println("\t\t\t This was tabbed three times");
        System.out.println("   this was tabbed withour escape");


        //examples of next line
        System.out.println();
        System.out.println("1) Go to the store");
        System.out.println("2) grab milk");
        System.out.println("3) wash car");

        System.out.println("\n1) Go in store \n2) Grab milk \n3) Wash car");


    }
}
