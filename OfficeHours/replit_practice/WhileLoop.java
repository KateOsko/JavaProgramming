package replit_practice;

public class WhileLoop {
    public static void main(String[] args) {

        int n=1;
        int n2=0;

        while (n<=20){
            System.out.print(n + " ");
            n+=2;
        }
        System.out.println();
        while (n2<=20) {
            System.out.print(n2 + " ");
            n2+=2;
        }

    }
}